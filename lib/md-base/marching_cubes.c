typedef struct {
  float vertices[3][3];
} triangle_t;

typedef struct {
  float position[3];
  float size[3];
  int values[8];
  int values2[4][4][4];
} grid_cell_t;

void normalize(float vec[3]) {
  float tmp;
  int i;
  tmp = 0;
  for (i = 0; i < 3; i++) {
    tmp+= vec[i]*vec[i];
  }
  tmp = sqrt(tmp);
  for (i = 0; i < 3; i++) {
    vec[i] /= tmp;
  }
}

static int grid_cell_to_triangles(const grid_cell_t *cell, int isolevel, triangle_t *triangles, triangle_t *normal_triangles) {
  int i;
  
  float cell_vertices[8][3];
  float vertex_list[12][3];
  float normal_list[12][3];
  unsigned char cube_index = 0;
  int num_triangles = 0;
  
  
  /* Determine the index into the edge table which
   * tells us which vertices are inside of the surface
   */
  for (i = 0; i < 8; i++) {
    if (cell->values[i] < isolevel) {
      cube_index |= (1 << i);
    }
  }
  
  /* Check whether the cube is entirely in-/outside the isovolume */
  if (!edge_table[cube_index]) {
    return 0;
  }
  
  /* Build cell_vertices */
  for (i = 0; i < 8; i++) {
#define o offset_table
    cell_vertices[i][0] = cell->position[0] + o[0][i]*cell->size[0];
    cell_vertices[i][1] = cell->position[1] + o[1][i]*cell->size[1];
    cell_vertices[i][2] = cell->position[2] + o[2][i]*cell->size[2];
#undef o
  }
  
  /* Find the vertices where the surface intersects the cube */
  for (i = 0; i < 12; i++) {
    if (edge_table[cube_index] & (1 << i)) {
      int a = i%8;
#define o offset_table
      int bs[12] = {1,2,3,0,5,6,7,4,4,5,6,7};
      int b = bs[i];
      
      /* Interpolate to find a vertex on the edge that has a value close to the 
       * isolevel and a fitting normal. The normal is the gradient of the volume 
       * (as the isosurface is always orthogonal to the gradient). */
      if (abs(cell->values[a] - isolevel) < 0.00001) {
        float grad[3];
        grad[0] = -cell->values2[o[0][a]+2][o[1][a]+1][o[2][a]+1]+cell->values2[o[0][a]][o[1][a]+1][o[2][a]+1];
        grad[1] = -cell->values2[o[0][a]+1][o[1][a]+2][o[2][a]+1]+cell->values2[o[0][a]+1][o[1][a]][o[2][a]+1];
        grad[2] = -cell->values2[o[0][a]+1][o[1][a]+1][o[2][a]+2]+cell->values2[o[0][a]+1][o[1][a]+1][o[2][a]];
        grad[0] *= -1;
        grad[1] *= -1;
        grad[2] *= -1;
        normalize(grad);
        memcpy(vertex_list[i],cell_vertices[a],3*sizeof(float));
        memcpy(normal_list[i],grad,3*sizeof(float));
      } else if (abs(cell->values[b] - isolevel) < 0.00001) {
        float grad[3];
        grad[0] = -cell->values2[o[0][b]+2][o[1][b]+1][o[2][b]+1]+cell->values2[o[0][b]][o[1][b]+1][o[2][b]+1];
        grad[1] = -cell->values2[o[0][b]+1][o[1][b]+2][o[2][b]+1]+cell->values2[o[0][b]+1][o[1][b]][o[2][b]+1];
        grad[2] = -cell->values2[o[0][b]+1][o[1][b]+1][o[2][b]+2]+cell->values2[o[0][b]+1][o[1][b]+1][o[2][b]];
        grad[0] *= -1;
        grad[1] *= -1;
        grad[2] *= -1;
        normalize(grad);
        memcpy(vertex_list[i],cell_vertices[b],3*sizeof(float));
        memcpy(normal_list[i],grad,3*sizeof(float));
      } else if (abs(cell->values[a] - cell->values[b]) < 0.00001) {
        float grad[3];
        grad[0] = -cell->values2[o[0][a]+2][o[1][a]+1][o[2][a]+1]+cell->values2[o[0][a]][o[1][a]+1][o[2][a]+1];
        grad[1] = -cell->values2[o[0][a]+1][o[1][a]+2][o[2][a]+1]+cell->values2[o[0][a]+1][o[1][a]][o[2][a]+1];
        grad[2] = -cell->values2[o[0][a]+1][o[1][a]+1][o[2][a]+2]+cell->values2[o[0][a]+1][o[1][a]+1][o[2][a]];
        grad[0] *= -1;
        grad[1] *= -1;
        grad[2] *= -1;
        normalize(grad);
        memcpy(vertex_list[i],cell_vertices[a],3*sizeof(float));
        memcpy(normal_list[i],grad,3*sizeof(float));
      } else {
        int j;
        float grad[3];
        float lambda = 1.0 * (isolevel - cell->values[a]) / (cell->values[b] - cell->values[a]);
        float grada[3];
        float gradb[3];
        grada[0] = -cell->values2[o[0][a]+2][o[1][a]+1][o[2][a]+1]+cell->values2[o[0][a]][o[1][a]+1][o[2][a]+1];
        grada[1] = -cell->values2[o[0][a]+1][o[1][a]+2][o[2][a]+1]+cell->values2[o[0][a]+1][o[1][a]][o[2][a]+1];
        grada[2] = -cell->values2[o[0][a]+1][o[1][a]+1][o[2][a]+2]+cell->values2[o[0][a]+1][o[1][a]+1][o[2][a]];
        gradb[0] = -cell->values2[o[0][b]+2][o[1][b]+1][o[2][b]+1]+cell->values2[o[0][b]][o[1][b]+1][o[2][b]+1];
        gradb[1] = -cell->values2[o[0][b]+1][o[1][b]+2][o[2][b]+1]+cell->values2[o[0][b]+1][o[1][b]][o[2][b]+1];
        gradb[2] = -cell->values2[o[0][b]+1][o[1][b]+1][o[2][b]+2]+cell->values2[o[0][b]+1][o[1][b]+1][o[2][b]];
        normalize(grada);
        normalize(gradb);
        for (j = 0; j < 3; j++) {
          vertex_list[i][j] = cell_vertices[a][j] + lambda * (cell_vertices[b][j] - cell_vertices[a][j]);
          grad[j] = grada[j] + lambda*(gradb[j]-grada[j]);
        }
        grad[0] *= -1;
        grad[1] *= -1;
        grad[2] *= -1;
        normalize(grad);
        memcpy(normal_list[i],grad,3*sizeof(float));
      }
#undef o
    }
  }
  /* Build triangles with the vertices and normals */
  for (i = 0; triangle_table[cube_index][i] != -1; i+=3) {
    memcpy(triangles[num_triangles].vertices[0],vertex_list[triangle_table[cube_index][i]],3*sizeof(float));
    memcpy(triangles[num_triangles].vertices[1],vertex_list[triangle_table[cube_index][i+1]],3*sizeof(float));
    memcpy(triangles[num_triangles].vertices[2],vertex_list[triangle_table[cube_index][i+2]],3*sizeof(float));
    /* Ignore triangles with 2 identical vertices (binary comparison instead of epsilon criterion, because even small gaps would look wrong) */
    if (!memcmp((void *)triangles[num_triangles].vertices[0],(void *)triangles[num_triangles].vertices[1],3*sizeof(float)))
      continue;
    if (!memcmp((void *)triangles[num_triangles].vertices[0],(void *)triangles[num_triangles].vertices[2],3*sizeof(float)))
      continue;
    if (!memcmp((void *)triangles[num_triangles].vertices[1],(void *)triangles[num_triangles].vertices[2],3*sizeof(float)))
      continue;
    memcpy(normal_triangles[num_triangles].vertices[0],normal_list[triangle_table[cube_index][i]],3*sizeof(float));
    memcpy(normal_triangles[num_triangles].vertices[1],normal_list[triangle_table[cube_index][i+1]],3*sizeof(float));
    memcpy(normal_triangles[num_triangles].vertices[2],normal_list[triangle_table[cube_index][i+2]],3*sizeof(float));
    num_triangles++;
  }
  return num_triangles;
}

